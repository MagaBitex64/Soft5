public class Student {
    private String name;
    private int CourseGrade;

    public void setCourseGrade(int courseGrade) {
        CourseGrade = courseGrade;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getCourseGrade() {
        return CourseGrade;
    }
}
